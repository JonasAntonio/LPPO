    public void listar() {
        try{
            dao = new AlunoDao();
            List<AlunoBean> lista = dao.listarAluno();
            DefaultTableModel modelo = (DefaultTableModel) this.tabela.getModel();
            modelo.setNumRows(0);
            
            for(AlunoBean a : lista){
                modelo.addRow(new Object[]{
                    a.getId(),
                    a.getNome(),
                    a.getDataMatricula(),
                    a.getDataNascimento(),
                    a.getEndereco(),
                    a.getTelefone(),
                    a.getAltura(),
                    a.getPeso()
                });
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Erro ao listar alunos!");
        }
    }
    
