    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try{
            AlunoBean a = new AlunoBean();
            a.setAltura(Float.parseFloat(txtAltura.getText()));
            a.setDataMatricula(txtDataMatricula.getText());
            a.setDataNascimento(txtDataNascimento.getText());
            a.setEndereco(txtEndereco.getText());
            a.setNome(txtNome.getText());
            a.setPeso(Float.parseFloat(txtPeso.getText()));
            a.setTelefone(txtTelefone.getText());
            
            dao = new AlunoDao();
            dao.adicionarAluno(a);
            
            JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
            limparCampos();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar aluno!");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

